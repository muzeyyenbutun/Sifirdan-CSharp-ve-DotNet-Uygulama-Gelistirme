CREATE TABLE KATEGORI (KATEGORI_REFNO INT NOT NULL PRIMARY KEY IDENTITY (1,1),KATEGORI_ADI VARCHAR (50) NOT NULL)

CREATE TABLE KITAP (KITAP_REFNO INT NOT NULL PRIMARY KEY IDENTITY (1,1),ADI VARCHAR (50) NOT NULL,YAZARI VARCHAR (50) NOT NULL,ALIS_FIYATI INT NOT NULL,SATIS_FIYATI INT NOT NULL,YAYIN_YILI NUMARIC (40) NOT NULL,KATEGORI_REFNO INT NULL FOREIGN KEY REFERENCES KATEGORI(KATEGORI_REFNO),OZET VARCHAR (200) NOT NULL)

CREATE TABLE UYE (UYE_REFNO INT NOT NULL PRIMARY KEY IDENTITY (1,1),ADI_SOYADI VARCHAR (50) NOT NULL,SIFRE VARCHAR (20) NOT NULL,DURUMU BIT NOT NULL)

CREATE TABLE DURUMU (DURUMU_REFNO INT NOT NULL PRIMARY KEY IDENTITY (1,1),ACIKLAMA VARCHAR (200) NOT NULL)

CREATE TABLE SIPARIS (SIPARIS_REFNO INT NOT NULL PRIMARY KEY IDENTITY (1,1),UYE_REFNO INT NOT NULL FOREIGN KEY REFERENCES UYE(UYE_UYE_REFNO),TARIH DATE NOT NULL,DURUMU_REFNO INT NOT NULL FOREIGN KEY REFERENCES DURUMU(DURUMU_REFNO)

CREATE TABLE SIP_DETAY (SIP_DETAY_REFNO INT NOT NULL PRIMARY KEY IDENTITY (1,1),SIPARIS_REFNO INT NOT NULL FOREIGN KEY REFERENCES SIPARIS(SIPARIS_REFNO),KITAP_REFNO INT OT NULL,ALIS_FIYATI INT NOT NULL,SATIS_FIYATI INT DEFAULT,ADET INT NOT NULL)

